public class Functions15 {
    static void function(int a){
        int r=a;
        int d=2*r;
        double c=2*3.14*r;
        double b=3.14*(int)Math.pow(r,2);
        System.out.println(d);
        System.out.println(c);
        System.out.println(b);
    }
    public static void main(String[] args) {
        function(4);
    }
}
