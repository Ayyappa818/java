public class Dimensionalarray {
    public static void main(String[] args) {
        int[][] ar={{20,14,4},{20,5,2},{3,9,11}};
        int sum=0;
        for(int i=0;i<=0;i++){
            for(int j=0;j<=2;j++){
                sum=sum+ar[i][j];
            }
        }
        System.err.println(sum);
    }
}
